static CMSCBB_ERROR_CODE GetRootCerf(CVB_BYTE **root, CVB_INT *rootLen)
{
    CVB_BYTE rootCert2[] = ROOT_G2_CERT_PART2;   /* 证书局部数据 */
    *rootLen = sizeof(pbRootG2Cert0) + sizeof(rootCert2);
    *root = (CVB_BYTE*)malloc(*rootLen);
    if (*root == CVB_NULL) {
        return CMSCBB_ERR_SYS_MEM_ALLOC;
    }

    if (memcpy_s(*root, *rootLen, pbRootG2Cert0, sizeof(pbRootG2Cert0))) {
        return CMSCBB_ERR_SYS_MEM_COPY;
    }

    if (memcpy_s(*root + sizeof(pbRootG2Cert0), *rootLen - sizeof(pbRootG2Cert0), rootCert2, sizeof(rootCert2))) {
        return CMSCBB_ERR_SYS_MEM_COPY;
    }

    return CVB_SUCCESS;
}

static void FreeCtxAndRoot(CMSCBB_VRF_CTX vrf_ctx, CVB_BYTE *root)
{
    if (vrf_ctx != CVB_NULL) {
        CmscbbVrfCtxFree(vrf_ctx);
        vrf_ctx = CVB_NULL;
    }

    if (root != CVB_NULL) {
        free(root);
        root = CVB_NULL;
    }
}

CMSCBB_ERROR_CODE AddRootCert(CVB_BYTE* root, CMSCBB_VRF_CTX vrfCtx)
{
    CMSCBB_ERROR_CODE ret;              /* 接口调用返回值 */
    CVB_INT rootLen;                    /* 证书总长度 */

    /* 组合根证书 */
    ret = GetRootCerf(&root, &rootLen);
    if (ret != CVB_SUCCESS) {
        FreeCtxAndRoot(vrfCtx, root);
        return CMSCBB_ERR_PKI_CERT_ADD_TO_STORE;
    }

    /* 添加根证书 */
    ret = CmscbbVerifyAddCert(vrfCtx, (const CVB_BYTE*)root, rootLen);
    if (ret != CVB_SUCCESS) {
        FreeCtxAndRoot(vrfCtx, root);
        return ret;
    }

    return CVB_SUCCESS;
}

